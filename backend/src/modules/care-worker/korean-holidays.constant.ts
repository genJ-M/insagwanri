/**
 * 한국 법정 공휴일 (근로기준법 제55조 유급 휴일 대상)
 * 연도별로 동적 계산 대신 정적 목록 사용 (성능·단순성 우선)
 * 매년 초에 업데이트 필요 (대체공휴일 포함)
 */
const HOLIDAYS_2025: string[] = [
  '2025-01-01', // 신정
  '2025-01-28', '2025-01-29', '2025-01-30', // 설날 연휴
  '2025-03-01', // 삼일절
  '2025-05-05', // 어린이날
  '2025-05-06', // 어린이날 대체공휴일
  '2025-06-03', // 현충일
  '2025-08-15', // 광복절
  '2025-10-03', // 개천절
  '2025-10-05', '2025-10-06', '2025-10-07', // 추석 연휴
  '2025-10-08', // 추석 대체공휴일
  '2025-10-09', // 한글날
  '2025-12-25', // 성탄절
];

const HOLIDAYS_2026: string[] = [
  '2026-01-01', // 신정
  '2026-02-16', '2026-02-17', '2026-02-18', // 설날 연휴
  '2026-03-01', // 삼일절
  '2026-03-02', // 삼일절 대체공휴일
  '2026-05-05', // 어린이날
  '2026-06-06', // 현충일
  '2026-08-15', // 광복절
  '2026-08-17', // 광복절 대체공휴일
  '2026-09-24', '2026-09-25', '2026-09-26', // 추석 연휴
  '2026-10-03', // 개천절
  '2026-10-09', // 한글날
  '2026-12-25', // 성탄절
];

const ALL_HOLIDAYS = new Set([...HOLIDAYS_2025, ...HOLIDAYS_2026]);

/**
 * 해당 날짜가 법정공휴일이면 true
 * @param dateStr YYYY-MM-DD (KST)
 */
export function isKoreanHoliday(dateStr: string): boolean {
  return ALL_HOLIDAYS.has(dateStr);
}

/**
 * 해당 날짜가 유급 휴일(일요일 or 법정공휴일)이면 true
 * → 가산수당 대상 판별에 사용
 */
export function isHolidayForPay(dateStr: string): boolean {
  const d = new Date(dateStr + 'T00:00:00');
  return d.getDay() === 0 || isKoreanHoliday(dateStr);
}
